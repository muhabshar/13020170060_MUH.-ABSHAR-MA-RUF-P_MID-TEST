package com.Abshar.TugasProject4.model;

public class CastCrew {
    String imageUrl;
    String name;

    public CastCrew(String imageUrl, String name) {
        this.imageUrl = imageUrl;
        this.name = name;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public String getName() {
        return name;
    }
}
